# formate specifiers

# [., :, :<, :>, :^, :+, :=, :, :,]

price1 = 3.1212
price2 = -987.23
price3 = 12.12

print(f"Price 1 is{price1}")
print(f"Price 2 is{price2}")
print(f"Price 3 is{price3}")

print(f"Price 1 is  {price1:0}")
print(f"Price 2 is  {price2:<10}")
print(f"Price 3 is  {price3:.3f}")

print(f"Price 3 is  {price3:,.3}")


